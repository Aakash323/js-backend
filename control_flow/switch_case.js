// switch case statement is used when there are multiple conditions
const day = 1
switch (day) {      // we use the variable that we have to test condition on
    case 1:
        console.log("the day is sunday");
        break;
    case 2:
        console.log("the day is Monday");
        break;
    case 3:
        console.log("the day is Tuesday");
        break;
    case 4:
        console.log("the day is Wednesday");
        break;
    case 5:
        console.log("the day is Thursday");
        break;
    case 6:
        console.log("the day is friday");
        break;
    case 7:
        console.log("the day is Saturday");
        break;
    default:
        console.log("choose a day from 1 to 7");
        break;
}